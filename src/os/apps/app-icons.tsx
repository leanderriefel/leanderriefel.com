interface AppIconProps extends IconProps {
  icon: string
}

// Component that renders the appropriate icon based on the icon string
export const AppIcon: Component<AppIconProps> = (props) => {
  const [local, rest] = splitProps(props, ["icon"])
  const IconComponent = () => iconMap[local.icon] ?? DefaultAppIcon

  return <Dynamic component={IconComponent()} {...rest} />
}
